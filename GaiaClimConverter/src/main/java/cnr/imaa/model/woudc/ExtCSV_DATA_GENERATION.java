package cnr.imaa.model.woudc;

public class ExtCSV_DATA_GENERATION {
    private String date;
    private String agency;
    private String version;
    private String scientificAuthority;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getAgency() {
        return agency;
    }

    public void setAgency(String agency) {
        this.agency = agency;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getScientificAuthority() {
        return scientificAuthority;
    }

    public void setScientificAuthority(String scientificAuthority) {
        this.scientificAuthority = scientificAuthority;
    }
}
