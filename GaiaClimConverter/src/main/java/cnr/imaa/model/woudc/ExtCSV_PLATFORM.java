package cnr.imaa.model.woudc;

public class ExtCSV_PLATFORM {
    private String type;
    private String id;
    private String name;
    private String country;
    private String gaw_id;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getGaw_id() {
        return gaw_id;
    }

    public void setGaw_id(String gaw_id) {
        this.gaw_id = gaw_id;
    }
}
